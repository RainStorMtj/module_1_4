my_string = input()
print(len(my_string)) # Вывод количества символов в слове
print(my_string.upper()) # Вывод в верхнем регистре
print(my_string.lower()) # Вывод в нижнем регистре
print(my_string.replace(' ', '')) # Удаление проблеов
print(my_string[0]) # Вывод первой буквы слова
print(my_string[-1]) # Вывод последний буквы слова